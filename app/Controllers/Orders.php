    public function make_order()
    {
        $model = model(OrdersModel::class);

        if ($this->validate([
            'name' => 'required',
            'email' => 'required|valid_email',
            'contact' => 'required',
            'location' => 'required',
            
        ])) {
            $name = $this->request->getPost('name');
            $email = $this->request->getPost('email');
            $contact = $this->request->getPost('contact');
            $location = $this->request->getPost('location');
            $comment = $this->request->getPost('comment');
            unset($_POST['name']);
            unset($_POST['email']);
            unset($_POST['contact']);
            unset($_POST['location']);
            unset($_POST['comment']);
            $selected_products = json_encode($_POST);
            $data = [
                'name' => $name,
                'email'  => $email,
                'contact'  => $contact,
                'location'  => $location,
                'selected_products'  => $selected_products,
                'comment'  => $comment,
            ];
            if($model->save($data)){
                if($this->checkIfUserOnline()){
                    //publishing@newvision.co.ug
                    $html_template = $this->email_template($data);
                    $to = 'ianmutungi00@gmail.com';
                    $email_instance = \Config\Services::email();
                    $email_instance->setTo($to);
                    $email_instance->setFrom($email, $name);
                    $email_instance->setSubject('[NV ESHOP]: New Order');
                    $email_instance->setMessage($html_template);
                    $headers = $email_instance->printDebugger(['headers']);
                    if($email_instance->send()){
                        echo json_encode(array('status' => true, 'msg' => 'Order sent', 'data'=> $headers));
                    }else{
                        
                        echo json_encode(array('status' => false , 'msg' => 'Order not received', 'data'=> $headers));
                    }
                }else{
                    echo json_encode(array('status' => false, 'msg' => 'Check Your Internet'));
                }
                
            }else{
                echo json_encode(array('status' => false));
            }
        }else{
            echo json_encode(array('status'=>false,'validation'=> $this->validator));
        }
    }

    public function checkIfUserOnline($site = 'https://google.com'){
        if(fopen($site, "r")){
            return true;
        }else{
            return false;
